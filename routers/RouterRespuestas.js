"use strict";

const path = require("path");
const express = require("express");

const ControllerRespuestas = require("../controllers/ControllerRespuestas");
var respuestas = express.Router();

/*
****************************************************************************************************************************************************************
                    INFOR PREGUNTA
****************************************************************************************************************************************************************                                                                   
*/

respuestas.get("/informacion_pregunta/:idPregunta", ControllerRespuestas.informacion_pregunta);

/*
****************************************************************************************************************************************************************
                RESPONDER PREGUNTA
****************************************************************************************************************************************************************                                                                   
*/

respuestas.post("/responderPregunta", ControllerRespuestas.responder_pregunta);

/*
****************************************************************************************************************************************************************
                VOTAR RESPUESTAS
****************************************************************************************************************************************************************                                                                   
*/

respuestas.post("/votarRespuesta",ControllerRespuestas.votar_respuesta);

module.exports = respuestas;